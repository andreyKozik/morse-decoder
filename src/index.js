const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    var morseDecoder = '';

    for (var i = 0; i < expr.length; i = i + 10) {
        var word = expr.slice(i, i + 10);

        if (word === '**********') {
            morseDecoder = morseDecoder + ' ';
        }  else {
         morseDecoder = morseDecoder + MORSE_TABLE[word.replace(/11/g, '-').replace(/10/g, '.').replace(/0/g, '')];
        }
    }

    return morseDecoder;
}

module.exports = {
    decode
}